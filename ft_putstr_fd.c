#include "libft.h"

/*
*	[DESCRIPTION]
*
*	Writes a string to the output.
*
*	[PARAMETERS]
*
*	char *s	: The string to write.
*	int fd	: The file descriptor.
*
*	[RETURN VALUE]
*
*	None.
*/

void	ft_putstr_fd(char *s, int fd)
{
	while (*s != '\0')
	{
		ft_putchar_fd(*s, fd);
		s++;
	}
}
